	pop100_2000 INTEGER, 
	hu100_2000 INTEGER, 
	h011d001 INTEGER, 
	h011d001_2000 INTEGER, 
	h011d002 INTEGER, 
	h011d002_2000 INTEGER, 
	h011d003 INTEGER, 
	h011d003_2000 INTEGER, 
	h011d004 INTEGER, 
	h011d004_2000 INTEGER, 
