-- H2. URBAN AND RURAL
-- designed to work with the IRE Census bulk data exports
-- see http://census.ire.org/data/bulkdata.html
CREATE TABLE ire_h2 (
	geoid VARCHAR(11) NOT NULL, 
	sumlev VARCHAR(3) NOT NULL, 
	state VARCHAR(2) NOT NULL, 
	county VARCHAR(3), 
	cbsa VARCHAR(5), 
	csa VARCHAR(3), 
	necta VARCHAR(5), 
	cnecta VARCHAR(3), 
	name VARCHAR(90) NOT NULL, 
	pop100 INTEGER NOT NULL, 
	hu100 INTEGER NOT NULL, 
	pop100_2000 INTEGER, 
	hu100_2000 INTEGER, 
	h002001 INTEGER, 
	h002001_2000 INTEGER, 
	h002002 INTEGER, 
	h002002_2000 INTEGER, 
	h002003 INTEGER, 
	h002003_2000 INTEGER, 
	h002004 INTEGER, 
	h002004_2000 INTEGER, 
	h002005 INTEGER, 
	h002005_2000 INTEGER, 
	h002006 INTEGER, 
	h002006_2000 INTEGER, 
	PRIMARY KEY (geoid)
);
